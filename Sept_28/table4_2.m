%% Example 1
m = 12
d = 7532.1234
fprintf('The value of m is %i',m)

%% Example 2
m = 12
d = 7532.1234
fprintf('The value of m is %i\n',m)

%% Example 3
m = 12
d = 7532.1234
fprintf('The value of d is %f\n',d)

%% Example 4
m = 12
d = 7532.1234
fprintf('The value of d is %.1f\n',d)

%% Example 5
m = 12
d = 7532.1234
fprintf('The value of d is %10.1f\n',d)

%% Example 6
m = 12
d = 7532.1234
fprintf('The value of d is %.2e\n',d)

%% Example 7
